class Operation
{
	public static void main(String args[ ])
	{
		int num1 = 20;
		int num2 = 10;
		int add = num1 + num2;
		int sub = num1 - num2;
		int mul = num1 * num2;
		int div = num1 / num2;
		System.out.println("Addition:"+add);
		System.out.println("subtraction:"+sub);
		System.out.println("Multiplication:"+mul);
		System.out.println("Division:"+div);
	}
}